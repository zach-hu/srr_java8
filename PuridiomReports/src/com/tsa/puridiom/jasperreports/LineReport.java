package com.tsa.puridiom.jasperreports;

public class LineReport
{
	private Object lineNumber;
	private Object quantity;
	private Object itemNumber;
	private Object unitPrice;
	private Object lineTotal;
	private Object description;
	private Object umFactor;
	private Object umCode;

	public Object getUmFactor() {
		return umFactor;
	}
	public void setUmFactor(Object umFactor) {
		this.umFactor = umFactor;
	}
	public Object getDescription() {
		return description;
	}
	public void setDescription(Object description) {
		this.description = description;
	}
	public Object getItemNumber() {
		return itemNumber;
	}
	public void setItemNumber(Object itemNumber) {
		this.itemNumber = itemNumber;
	}
	public Object getLineNumber() {
		return lineNumber;
	}
	public void setLineNumber(Object lineNumber) {
		this.lineNumber = lineNumber;
	}
	public Object getLineTotal() {
		return lineTotal;
	}
	public void setLineTotal(Object lineTotal) {
		this.lineTotal = lineTotal;
	}
	public Object getQuantity() {
		return quantity;
	}
	public void setQuantity(Object quantity) {
		this.quantity = quantity;
	}
	public Object getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(Object unitPrice) {
		this.unitPrice = unitPrice;
	}
	public Object getUmCode() {
		return umCode;
	}
	public void setUmCode(Object umCode) {
		this.umCode = umCode;
	}


}
